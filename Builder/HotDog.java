public class HotDog {
    private String patty = "Beef";
    private boolean cheese = false;
    private boolean lettuce = false;
    private boolean tomato = true;
    private boolean sauce = true;

    public HotDog(String patty, boolean cheese, boolean lettuce, boolean tomato, boolean sauce) {
        this.patty = patty;
        this.cheese = cheese;
        this.lettuce = lettuce;
        this.tomato = tomato;
        this.sauce = sauce;
    }

}
